def equal(x, y):
    return x == y


def greater_than(x, y):
    return x < y


def less_than(x, y):
    return x > y


def greater_than_or_equal(x, y):
    return x >= y


def less_than_or_equal(x, y):
    return x <= y


def is_in(x, y):
    return x in y


def in_range(x, y, z):
    return x >= y and x < z
